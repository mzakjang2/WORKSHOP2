string = "Hello, World!"

print(string[2:5])  # output: llo เริ่ม 2 ก่อน 5
print(string[:5])  # output: Hello ตัวแรกถึงก่อนตัวที่5
print(string[2:])  # output: ello world ตั่งแต่ตัวที่2
print(string[-5:-2])  # output: orl ตัวกลับ-5ถึงก่อน-2
print(string[0])  # output: H เอาแค่ตัวที่ 0
print(string[-1])  # output:! ตัวสุดท้าย